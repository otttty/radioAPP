// ============================================================
// cleanApiKey
// ------------------------------------------------------------
// APIキー文字列から、コピペや環境変数登録時に紛れ込む不可視文字
// (前後の空白・改行・タブ・U+2028/U+2029 などの制御/区切り文字)を除去する。
//
// なぜ必要か: これらの文字がキーに混じると、HTTPヘッダ(例:
// "X-Goog-Api-Key" や "Authorization: Bearer ...")に載せた瞬間に
// fetch が "Cannot convert argument to a ByteString ..." で例外を投げ、
// リクエストが一切通らなくなる。ヘッダに使えるのは 0x00-0xFF の範囲だが、
// 実際のキーは印字可能ASCII(0x21-0x7E)のみなので、それ以外を取り除く。
// ============================================================

/**
 * @param {unknown} raw
 * @returns {string} 印字可能ASCIIだけを残したキー(不正/空なら空文字)
 */
export function cleanApiKey(raw) {
  if (typeof raw !== 'string') return '';
  // 印字可能ASCII(スペース0x20を含まない 0x21-0x7E)以外をすべて除去する。
  // 実在のAPIキーは英数字と - _ のみで構成されるため、これで安全に洗える。
  return raw.replace(/[^\x21-\x7E]/g, '');
}
